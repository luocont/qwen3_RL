def generate_dataset(output_path: str, num_samples: int = None):
    data = INTAKE_FORMS
    if num_samples is not None:
        data = data[:num_samples]

    os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)

    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"数据集已生成：{output_path}，共 {len(data)} 条样本。")


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--output_path", type=str, default="data/counseling_dataset.json")
    parser.add_argument("--num_samples", type=int, default=None)
    args = parser.parse_args()

    generate_dataset(args.output_path, args.num_samples)
